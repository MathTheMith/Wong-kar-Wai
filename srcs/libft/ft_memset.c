#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		((uint8_t *)s)[i] = (uint8_t)c;
		i++;
	}
	return (s);
}
