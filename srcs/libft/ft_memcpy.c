#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t	i;

	if (!dest && !src)
		return (NULL);
	i = 0;
	while (i < n)
	{
		((uint8_t *)dest)[i] = ((uint8_t *)src)[i];
		i++;
	}
	return (dest);
}
