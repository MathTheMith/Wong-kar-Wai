#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i])
		i++;
	while (i >= 0)
	{
		if ((uint8_t)s[i] == (uint8_t)c)
			return ((char *)&s[i]);
		i--;
	}
	return (NULL);
}
